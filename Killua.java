    // The Lightning class is the class for creating the aura of lightning around Killua. It makes use of Line objects to draw and animate lightning.
    private class Lightning {
        Line l1, l2, l3, l4, l5, l6;

        // Draws the lightning based on the current keyframe
        void draw(Graphics2D g2d) {
            l1 = new Line(x1,y1,x1+20,y1+20,4,lightningColor1);
            l2 = new Line(x1+20,y1+20,x1-20,y1+60,4,lightningColor1);
            l3 = new Line(x1,y1+40,x1+10,y1+60,2,lightningColor2);
            l4 = new Line(x1-20,y1+60,x1+20,y1+90,4,lightningColor2);
            l5 = new Line(x1,y1+80,x1-15,y1+90,2,lightningColor3);
            l6 = new Line(x1+5,y1+80,x1,y1+120,2,lightningColor3);

            switch(keyFrame) {
                case 6:
                    l6.draw(g2d);
                case 5:
                    l5.draw(g2d);
                case 4:
                    l4.draw(g2d);
                case 3:
                    l3.draw(g2d);
                case 2:
                    l2.draw(g2d);
                case 1:
                    l1.draw(g2d);
            }
        } 
    }

    // Repeatedly adds and decreases Killua's y-coordinate to create the illusion of bouncing while riding his skateboard
    public void bounce() {
        if (keyFrame < 4) {
            y1 += 1.5;
        } else {
            y1 -= 1.5;
        }
    }

    // Makes Killua face right
    @Override
    public void moveRight() {
        flip = true;
    }

    // Makes Killua face left
    @Override
    public void moveLeft() {
        flip = false;
    }

    // Draws all of Killua's parts depending on whether he is facing to the left or to the right
    public void draw(Graphics2D g2d) {
        AffineTransform reset = g2d.getTransform();
        g2d.scale(0.8,0.8);
        g2d.translate(0,80);
        AffineTransform left = g2d.getTransform();
        AffineTransform right = g2d.getTransform();
        right.scale(-1,1);
        right.translate(-800,0);

        if (flip) { 
            for (int i = 0; i < 3; i++) {
                g2d.rotate(Math.toRadians(90), x1,y1);
                g2d.translate(75 + (i * 45), -20);
                lightningFX.draw(g2d);
                g2d.setTransform(left);
            }
            for (int i = 0; i < 3; i++) {
                g2d.rotate(Math.toRadians(90), x1,y1);
                g2d.translate(75 + (i * 45), 60);
                lightningFX.draw(g2d);
                g2d.setTransform(left);
            }
            g2d.setTransform(right);
            killuaLegs.draw(g2d, -x1+700, y1);
            killuaBody.draw(g2d, -x1+700, y1);
            killuaHead.draw(g2d, -x1+700, y1);
        } else {
            for (int i = 0; i < 3; i++) {
                g2d.rotate(Math.toRadians(-90), x1,y1);
                g2d.translate(-80 + (i * -45), 80);
                lightningFX.draw(g2d);
                g2d.setTransform(left);
            }
            for (int i = 0; i < 3; i++) {
                g2d.rotate(Math.toRadians(-90), x1,y1);
                g2d.translate(-80 + (i * -45), 160);
                lightningFX.draw(g2d);
                g2d.setTransform(left);
            }
            killuaLegs.draw(g2d, x1, y1);
            killuaBody.draw(g2d, x1, y1);
            killuaHead.draw(g2d, x1, y1);
        }
        g2d.setTransform(reset);
    }
